#include "libft.h"

int			ft_atoi(const char *str)
{
	size_t	i;
	size_t	a;
	size_t	dst;

	i = 0;
	a = 1;
	dst = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '+')
		i++;
	else if (str[i] == '-')
	{
		a = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		dst = dst * 10 + (str[i] - '0');
		i++;
	}
	return (dst * a);
}
